const SECOND = 1000
const MINUTE =  60 * SECOND
const HOUR   =  60 * MINUTE
const DAY    =  24 * HOUR
const WEEK   =   7 * DAY
const MONTH  =  31 * DAY    // aproximately
const YEAR   = 365 * DAY    // aproximately

module.exports = {
    SECOND: SECOND,
    MINUTE: MINUTE,
    HOUR: HOUR,
    DAY: DAY,
    WEEK: WEEK,
    MONTH: MONTH,
    YEAR: YEAR,
}
